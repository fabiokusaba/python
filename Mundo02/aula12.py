#Condições Aninhadas
#Condições aninhadas são estruturas condicionais dentro de estruturas condicionais.
#Dentro de um if você pode utilizar quantos elif você quiser, mas você não pode ter um elif sem um if e o else você só pode utilizar uma ou nenhuma vez.
'''Bloco de código
if <condição>:
    bloco 1
elif <condição>:
    bloco 2
else:
    bloco 3
'''