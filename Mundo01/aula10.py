#Condições (Parte 1)
#Condições simples e compostas
#Numa condição ou o bloco verdadeiro é executado ou o bloco falso é executado nunca serão executados ao mesmo tempo.
#Para estruturas simples utilizamos apenas o if e para estruturas compostas utilizamos if else.
#Temos ainda a condição simplificada que pode ser representada da seguinte forma:

'''Bloco de código
print('carro novo' if tempo <=3 else 'carro velho')
'''
#Dentro do if nós vamos colocar o bloco verdadeiro e dentro do else nós vamos colocar o bloco falso ficando representado da seguinte forma:

'''Bloco de código
if <condição>:
    bloco True
else:
    bloco False
'''