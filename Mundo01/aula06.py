#Tipos Primitivos e Saída de Dados
#Int (int) número inteiro, todos aqueles que não possuem vírgula, exemplo: 7, -4, 0, 9875, então o número negativo, positivo ou nulo seja ele o valor que for são inteiros para o Python
#Float (float) números reais ou números de ponto flutuante, por exemplo: 4.5, 0.076, -15.223, 3.14, 7.0
#Bool (bool) lógicos ou booleanos, só aceitam dois valores o valor verdadeiro (True) ou o valor falso (False)
#Str (str) caracteres ou strings, por exemplo: 'Olá', '7.5', ''