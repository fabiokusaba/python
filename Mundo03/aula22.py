#Módulos e Pacotes
#Modularização - modularizar é o ato de construir módulos.
#Foco em dividir um programa grande.
#Foco em aumentar a legibilidade.
#Foco em facilitar a manutenção.
#Vantagens de se ter uma modularização:
#->Organização de código;
#->Facilidade na manutenção;
#->Ocultação do código detalhado;
#->Reutilização em outros projetos.
#Quando só os módulos não puderem te satisfazer porque ficou muito grande a gente tem a solução dentro do Python que chamamos de pacotes em outras linguagens de programação são chamados de biblioteca.
#O Python colocou além da criação de outros módulos a junção de módulos e separados por assunto que é o que chamam de pacote.
#O pacote nada mais é do que uma pasta que contém módulos então ao invés de termos um uteis.py eu vou ter um Pacote uteis com as mesmas funções só que agora eu posso separar por assunto, dentro do meu Pacote uteis eu posso ter funções, por exemplo: só para tratamento de números, funções para tratamento de strings, funções compatíveis com datas, e outras para manipulação de cores.