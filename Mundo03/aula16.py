#Variáveis Compostas (Tuplas)
#Em Python existem três tipos de variáveis compostas: as tuplas, as listas e os dicionários.
#variáveis simples significam que elas só tem espaço na memória para uma coisa. 
#Já as variáveis compostas podem armazenar vários valores.
#Os elementos na tupla são identificados por índices, índices são numéricos e no Python iniciam em 0 e vão até o final.
#Dentro de tuplas podem utilizar o que já vimos sobre fatiamento. Podemos ainda utilizar o método len() para sabermos do seu comprimento.
#Outra coisa muito importante que pode ser feito no estudo das variáveis compostas é o uso das estruturas de repetição.
#As tuplas são IMUTÁVEIS! Isso significa que elas não podem ser mudadas, uma vez definidas elas vão ficas assim até o final do programa. 
